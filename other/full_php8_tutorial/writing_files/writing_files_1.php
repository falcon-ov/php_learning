//Подсказка:
//Используйте file_put_contents($filename, $data) для записи.
//Функция is_writable($filename) проверяет, доступен ли файл для записи.
//Убедитесь, что права на запись в директории позволяют создавать или изменять файл.
$filename = 'output.txt';
$data = "Это новый текст!";
if(is_writable($filename)){
    $succes = file_put_contents($filename, $data);
}else{
    echo "Файл не доступен для записи";
}
if(!$succes){
    echo "Ошибка при записи";
}

