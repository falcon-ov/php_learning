//для записи строки "Это новый текст!" в файл output.txt,
//перезаписывая его содержимое. Если запись прошла успешно,
//выведите сообщение об успехе. Если файл не удалось записать,
//выведите сообщение об ошибке. Проверьте, существует ли файл,
//перед записью с помощью is_writable().
